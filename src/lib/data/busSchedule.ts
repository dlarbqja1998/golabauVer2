// 4. 오늘의 시간표 가져오기 함수
export function getTodaySchedule() {
    const now = new Date();
    const day = now.getDay(); // 0:일, 1:월, ... 5:금, 6:토

    if (day === 0) return sundaySchedule; // 일요일
    if (day === 6) return null;           // 토요일 (운행 없음)
    if (day === 5) return fridaySchedule; // 금요일
    return weekdaySchedule;               // 월~목
}